## type Function (!) interactive mode required
type(1)         # determines the data type of the argument, int
type(2.4)       # determines the data type of the argument, float
type('string')  # determines the data type of the argument, str
type(True)      # determines the data type of the argument, bool
type([2, 3, 4]) # determines the data type of the argument, list
type((2, 3, 4)) # determines the data type of the argument, tuple