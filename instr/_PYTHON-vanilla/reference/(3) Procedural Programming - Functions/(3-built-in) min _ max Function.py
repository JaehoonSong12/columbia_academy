## min and max Function
print(min([1, 2, 3, 4]))    # returns the minimum value of a list (arg: list, return: data)
print(max([1, 2, 3, 4]))    # returns the minimum value of a list (arg: list, return: data)
print(min((1, 2, 3, 4)))    # returns the minimum value of a tuple (arg: tuple, return: data)
print(max((1, 2, 3, 4)))    # returns the minimum value of a tuple (arg: tuple, return: data)
