## len Function
print(len([2, 3]))                      # returns the length of a list          (arg: list, return: int)
print(len((2,)))                        # returns the length of a tuple         (arg: tuple, return: int)
print(len(range(3)))                    # returns the length of a iterable      (arg: iterable, return: int)
print(len('string'))                    # returns the length of a string        (arg: str, return: int)
print(len({'key1': 10, 'key2': 20}))    # returns the length of a dictionary    (arg: dictionary, return: int)
print(len(set([1, 2, 3])))              # returns the length of a set           (arg: set, return: int)