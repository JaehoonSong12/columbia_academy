## Expressions with Comma Operator
x = 2, 3    # returns a tuple if the expression is not used as function arguments (return: tuple)
print(x)

y = 2, 3, 4 # equivalent to y = (2, 3, 4)
print(y)

## Multiple Assignment with Comma Operator
x, y = 2, 3 # equivalent to (x, y) = (2, 3)
print(x)
print(y)
