## math Module
import math         # load the math module into memory


## Library Functions (Black Boxes)
# pi
print(math.pi)              # returns the value of pi (return: float)

# e
print(math.e)               # returns the value of e, natural number (return: float)

# ceil Function
print(math.ceil(3.5))       # returns an integer rounded up (argument: float, return: int)

# floor Funtion
print(math.floor(3.5))      # returns an integer rounded down (argument: float, return: int)


# degrees Function
print(math.degrees(math.pi))# returns converted value of pi radians in degrees (argument: float, return: float)

# radians Function
print(math.radians(180))    # returns converted value of 180 degrees in radians (argument: float, return: float)


# sin Function
print(math.sin(math.pi))    # returns the sine of pi in radians (argument: float, return: float)

# cos Function
print(math.cos(math.pi))    # returns the cosine of pi in radians (argument: float, return: float)

# tan Function
print(math.tan(math.pi))    # returns the tangent of pi in radians (argument: float, return: float)

# asin Function
print(math.asin(0.5))       # returns the inverse sine of 0.5 in radians (argument: float, return: float)

# acos Function
print(math.acos(0.5))       # returns the inverse cosine of 0.5 in radians (argument: float, return: float)

# atan Function
print(math.atan(0.5))       # returns the inverse tangent of 0.5 in radians (argument: float, return: float)


# exp Function
print(math.exp(2))          # returns the e to the power of 2 (argument: float, return: float)

# log Function
print(math.log(math.e))     # returns the ln of e (argument: float, return: float)

# log10 Function
print(math.log10(100))      # returns the log of 100 (argument: float, return: float)


# sqrt Function
print(math.sqrt(4))         # returns the square root of 4 (argument: float, return: float)

# hypot Function
print(math.hypot(3, 4))     # returns the distance from the origin in the coordinate plane (argument1: float, argument2: float, return: float)
